public class AvionMilitar extends Avion {

    private boolean xifrat;
    private String bandol;

    public boolean isXifrat() {
        return xifrat;
    }

    public void setXifrat(boolean xifrat) {
        this.xifrat = xifrat;
    }
    public String getBandol() {
        return bandol;
    }

    public void setBandol(String bandol) {
        this.bandol = bandol;
    }
}